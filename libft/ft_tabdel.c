#include "libft.h"

void		ft_tabdel(char **tab)
{
	int		i;

	i = -1;
	if (tab && *tab)
	{
		while (tab[++i])
		{
			ft_strdel(&tab[i]);
		}
	}
	if (tab)
		free(tab);
	tab = NULL;
}
